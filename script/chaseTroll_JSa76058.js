// function moveTroll to move troll image
function moveTroll(){
    var trollImg = document.getElementById("trollImg");
    trollImg.style.float = 'right';
} // end moveTroll function

// function moveTrollBack to move troll image to start position
function moveTrollBack(){
    var trollImg = document.getElementById("trollImg");
    trollImg.style.float = 'left';
} // end moveTrollBack function